let a = 10;
// operadores relacionales
console.log(a > 5); 
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

// operadores de igualdad
console.log(a == 10); // igualdad de valores
console.log(a != 10);
console.log(a == '10');
console.log(a === '10'); // triple igualdad consulta por valor y tipo de dato
console.log(a !== '10');